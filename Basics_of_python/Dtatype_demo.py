# program about variables in python are dynamically typed

# In pyton variables doesn't have any data type . The datatypes of varible is dependent on the type of value it is stored

x=15
print(type(x))       #output : <class 'int'>

x=3.14
print(type(x))      #output : <class 'float'>

x="madhu"
print(type(x))   #output : <class 'str'>

x=[1,2,3,4]
print(type(x))      #output : <class 'list'>

#In above instructions the type of x is changed based on the value stored
#Hence python variables are dynamically typed